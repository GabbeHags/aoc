pub fn part_2(input: &str) -> u32 {
    todo!()
}

mod test {
    #![allow(unreachable_code, unused_imports)]
    use super::*;

    #[allow(dead_code)]
    fn get_example_input() -> &'static str {
        ""
    }

    #[allow(dead_code)]
    fn get_real_input() -> &'static str {
        include_str!("../input/2022/day3.txt")
    }

    #[test]
    fn test_example_part1() {
        assert_eq!(part_1(&get_example_input()), todo!());
    }

    #[test]
    fn test_example_part2_() {
        assert_eq!(part_2(&get_example_input()), todo!());
    }

    #[test]
    fn test_real_part1() {
        assert_eq!(part_1(&get_real_input()), todo!());
    }

    #[test]
    fn test_real_part2() {
        assert_eq!(part_2(&get_real_input()), todo!());
    }
}
